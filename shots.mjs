import { chromium } from "@playwright/test";
const b = await chromium.launch();
const page = await b.newPage({ viewport: { width: 1380, height: 900 } });
page.setDefaultTimeout(90000);
await page.goto("http://localhost:3000/login", { timeout: 90000 });
await page.locator('input').first().fill("admin@ourdomain.com");
await page.getByRole("button", { name: /continue/i }).click();
await page.waitForURL("http://localhost:3000/", { timeout: 90000 });
await page.waitForTimeout(6000);
await page.screenshot({ path: "/tmp/dashboard.png" });
await page.goto("http://localhost:3000/shipments");
await page.waitForTimeout(6000);
await page.screenshot({ path: "/tmp/shipments.png" });
await page.locator("tbody a").first().click();
await page.waitForTimeout(6000);
await page.screenshot({ path: "/tmp/shipment-detail.png", fullPage: true });
await page.setViewportSize({ width: 390, height: 844 });
await page.goto("http://localhost:3000/receive");
await page.waitForTimeout(4000);
await page.screenshot({ path: "/tmp/receive-mobile.png" });
await b.close();
console.log("SHOTS OK");
