import { chromium } from "@playwright/test";
const b = await chromium.launch();
const page = await b.newPage({ viewport: { width: 1380, height: 900 } });
page.setDefaultTimeout(90000);
await page.goto("http://localhost:3000/login", { timeout: 90000 });
await page.waitForTimeout(1500);
await page.screenshot({ path: "/tmp/lux-login.png" });
await page.locator('#email').fill("admin@ourdomain.com");
await page.locator('#password').fill("Icon@1234");
await page.getByRole("button", { name: "Sign in" }).click();
await page.waitForURL("http://localhost:3000/", { timeout: 90000 });
await page.waitForTimeout(4000);
await page.screenshot({ path: "/tmp/lux-dash.png" });
await page.goto("http://localhost:3000/samples");
await page.waitForTimeout(4000);
await page.screenshot({ path: "/tmp/lux-samples.png" });
// factories: verify New factory dialog works end to end
await page.goto("http://localhost:3000/factories");
await page.waitForTimeout(2500);
await page.getByRole("button", { name: "New factory" }).click();
await page.waitForTimeout(600);
await page.locator('#name').fill("Test Atelier Co");
await page.getByRole("button", { name: "Add factory" }).click();
await page.waitForURL(/factories\/.+/, { timeout: 30000 });
console.log("FACTORY CREATED OK");
await b.close();
